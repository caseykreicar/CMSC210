#how to find hypotenuse of a right sided triangle FLOATING POINT
a = float(input("Please enter the lenth (in decimal form) of one side of the right triangle: "))
b = float(input("Please enter the length (in decimal form) of the other side of the right triangle: "))
c = (a ** 2 + b ** 2) ** 0.5
print("c =", c)

